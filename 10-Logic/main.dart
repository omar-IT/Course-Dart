void main() {
// * Logical and ---> &&
  print('-------Logical AND-------');
  print('true && false = ${true && false}');
  print('true && true = ${true && true}');

// * Logical or ---> ||
  print('-------Logical OR-------');
  print('true || false = ${false || true}');
  print('false || false = ${false || false}');

// * Logical or ---> !
  print('-------Logical NOT-------');
  print('!true  => ${!true}');
  print('!false => ${!false}');

}
