void main() {
// * Greater Then
  print('Greater Then 5 > 3 : ${5 > 3}');

// * Less Then
  print('Lesss Then 5 < 3 : ${5 < 3}');

// * Greater Then Or Equal
  print('Greater Then Or Equal 5 >= 3 : ${5 >= 3}');

// * Less Then Or Equal
  print('Less Then Or Equal 5 <= 3 : ${5 <= 3}');

// * Is Equal To
  print('Is Equal To 5 == 3 : ${5 == 3}');

// * Is Not Equal To
  print('Is Not Equal To 5 != 3 : ${5 != 3}');
}
